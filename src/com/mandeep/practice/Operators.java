package com.mandeep.practice;

public class Operators {

    public void Increment(){

        int u,v,w,x;

        u = 1;
        v = u++;
        w = 1;
        x = ++w;

        System.out.println("u = " + u);
        System.out.println("v = " + v);
        System.out.println("w = " + w);
        System.out.println("x = " + x);
    }
}
